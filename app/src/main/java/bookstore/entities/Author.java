package bookstore.entities;

public class Author {
    private int id;
    private String name;

    public Author() {
    }

    public int getId() {
        return id;
    }
    public void setId(int value) {
        id = value;
    }

    public String getName() {
        return name;
    }
    public void setName(String value) {
        name = value;
    }


}
